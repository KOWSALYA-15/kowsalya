package guvi;

public class PrintASCIIValues {

	public static void main(String[] args) {
  	  System.out.println("a to z:");
      for(int i='a';i<='z';i++){
    	  System.out.println((int)i);
      }
	  System.out.println("A to Z:");
      for(int i='A';i<='Z';i++){
    	  System.out.println((int)i);
      }
	}

}
